// Smooth scroll için utility fonksiyon
export const smoothScrollTo = (target: string | HTMLElement, duration: number = 1) => {
  gsap.to(window, {
    duration,
    scrollTo: { y: target, offsetY: 80 },
    ease: 'power2.inOut',
  });
};

// Sayfa yüklendiğinde smooth scroll'u etkinleştir - sadece client-side'da
export const initSmoothScroll = () => {
  // Sadece client-side'da çalıştır
  if (typeof window !== 'undefined') {
    // Smooth scroll için CSS scroll-behavior kullan
    document.documentElement.style.scrollBehavior = 'smooth';
  }
};
